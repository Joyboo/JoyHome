/**
 * menu: 菜单，是menu数据表的直接表现
 * router： 路由，由menu数据变化而来，必须是符合Vue Router标准格式的数据
 * 实际上menu与router是由一张数据表管理的，但对前端来说，两者是没有任何关系的
 * 如设置面板中的"TOP菜单模式"设置两种不同的菜单模式，菜单是允许任意变化的，但路由格式必须为符合Vue Router的格式
*/

/**
 * 从list中递归找到某个菜单
 * @param id
 * @param list
 * @returns {any}
 */
const findMenu = (id, list) => {
  for (const item of list) {
    if (item.id == id) {
      return item
    } else if (typeof item.children !== 'undefined') {
      const fd = findMenu(id, item.children)
      if (fd) return fd
    }
  }
  return false
}

/**
 * 根据list，递归生成级联选择器数据
 * @param list
 * @returns {[]}
 */
const makeCascader = list => {
  const cascader = []
  list.forEach(item => {
    const ca = {
      value: item.id,
      label: item.title
    }
    if (typeof item.children !== 'undefined') {
      ca['children'] = makeCascader(item.children)
    }
    cascader.push(ca)
  })
  return cascader
}

