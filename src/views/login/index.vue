export default {
  name: 'Login',
  components: { LangSelect, SocialSign, BubbleBg },
  data() {
    // todo 校验账号和密码，因为Eslint的关系已删除了，请自行实现
    // 本地开发环境
    const local = process.env.NODE_ENV === 'development'
    return {
      local: local,
      loginForm: {
        username: 'Joyboo',
        password: '123456'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur'/*, validator: validateUsername*/ }],
        password: [{ required: true, trigger: 'blur'/*, validator: validatePassword */ }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {},
      bgHeight: '',
      isMobile: false
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  created() {
    // window.addEventListener('storage', this.afterQRScan)
    window.addEventListener('resize', this.setBgHeight)
    this.setBgHeight()
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }

    // el-carouse自动切换设置后，当鼠标hover时，就会暂停，此处实现为永久性定时切换
    const timer = setInterval(() => {
      if (typeof this.$refs.loginCarouse != 'undefined') {
        this.$refs.loginCarouse.next()
      }
    }, 2000)
    // hool删除定时器
    this.$once('hook:beforeDestroy', () => {
      clearInterval(timer)
    })
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
    window.removeEventListener('resize', this.setBgHeight)
  },
  methods: {
    setBgHeight() {
      this.bgHeight = calcHeight(0) + 'px'
      const rect = window.document.body.getBoundingClientRect()
      this.isMobile = rect.width - 1 < 992
    },
    checkCapslock(e) {
      const { key } = e
      this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm)
            .then(() => {
              this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
              this.loading = false
            })
            .catch((error) => {
              this.loading = false
              this.$message.error(error)
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
    // afterQRScan() {
